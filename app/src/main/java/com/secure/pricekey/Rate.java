package com.secure.pricekey;

public class Rate {
    String gold;
    String silver;
    String platinum;
    String palladium;
    String copper;
    String aluminum;
    String nickel;
    String zinc;

    public Rate(String gold, String silver, String platinum, String palladium, String copper, String aluminum, String nickel, String zinc){
        this.gold = gold;
        this.silver = silver;
        this.platinum = platinum;
        this.palladium = palladium;
        this.copper = copper;
        this.aluminum = aluminum;
        this.nickel = nickel;
        this.zinc = zinc;
    }
}
